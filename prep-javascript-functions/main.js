function addTwoNumbers(x, y) {
  return x + y;
}

var addTwoNumbersResult = addTwoNumbers(3, 9);
console.log('Add two numbers result', addTwoNumbersResult);

function convertHoursToMinutes(hours) {
  return hours * 60;
}
var hoursToMinutesResult = convertHoursToMinutes(3);
console.log('Convert hours to minutes result', hoursToMinutesResult);

